public class Calculadora {
    public int somarDoisNumeros(int num1, int num2){
        return num1+num2;
    }
    public int subtrairDoisNumeros(int n1, int n2){
        return n1 - n2;
    }
    public int multiplicarDoisNumeros(int valor1, int valor2){
        return valor1 * valor2;
    }

    public int dividirDoisNumeros(int numeroUm, int numeroDois){
        return numeroUm / numeroDois;
    }
}
